for (let i = 1; i <= 10; i++) {
  if (i % 2 !== 0) {
    console.log(i + " is an odd number");
  }
}

console.log("\n")

//while loop
let i = 1;

while (i <= 10) {
  if (i % 2 !== 0) {
    console.log(i + " is an odd number");
  }
  i++;
}
console.log("\n");
//do - while loop
let o = 1;

do {
  if (o % 2 !== 0) {
    console.log(o + " is an odd number");
  }
  o++;
} while (o <= 10);
